// Operadores aritméticos básicos:
// + → soma
// - → subtração
// * → multiplicação
// / → divisão

// Operadores adicionais:
// % → resto da divisão (módulo)
// ** → exponenciação (potência)

// Exibe o resultado da soma
console.log("10 + 2 = ", 10 + 2); // 12

// Exibe o resultado da subtração
console.log("10 - 2 = ", 10 - 2); // 8

// Exibe o resultado da multiplicação
console.log("10 * 2 = ", 10 * 2); // 20

// Exibe o resultado da divisão
console.log("10 / 2 = ", 10 / 2); // 5

// Exibe o resto da divisão de 10 por 2
console.log("10 % 2 = ", 10 % 2); // 0

// Exibe 10 elevado a 2
console.log("10 ** 2 = ", 10 ** 2); // 100
